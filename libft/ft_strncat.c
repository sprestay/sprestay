#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t	j;
	size_t	i;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (j < n && src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
